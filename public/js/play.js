function play(){

  //using JQuery's extend function to copy array value and not the reference
  var navNodes = $.extend(true, [], navDiagram.model.nodeDataArray),
      navLinks = $.extend(true, [], navDiagram.model.linkDataArray),
      pages = [],
      forms = [],
      lists = [],
      routes = "";

  //Divide by category
  for ( var i = 0; i < navNodes.length; i++){

      if ( navNodes[i].category == "MainPage" || navNodes[i].category == "Page" ){
        pages.push(navNodes[i]);
      } else if ( navNodes[i].category == "Form" ){
        forms.push(navNodes[i]);
      } else if ( navNodes[i].category == "List"){
        lists.push(navNodes[i]);
      }
  }

  for ( var p = 0; p < pages.length; p++ ){

    var pageForms = '',
        pageLists = '',
        pageLinks = '',
        content =  '';

    for ( var f = 0; f < forms.length; f++ ){
      //parse forms within group
      if( forms[f].group == pages[p].key ){
        pageForms = pageForms + parseForm(forms[f]) + '\n';
      }
    }

    for ( var l = 0; l < lists.length; l++ ){
      //parse lists within group
       if ( lists[l].group == pages[p].key ){
        pageLists = pageLists + parseList(lists[l]) + '\n';
      }
    };

    for( var k = 0; k < navLinks.length; k++ ){
      //parse forms within group
      if(navLinks[k].from == pages[p].key ){
        pageLinks = pageLinks + parseLink(navLinks[k], pages) + '\n';
      }
    };

    content =  pageForms + pageLists + pageLinks;

    //Create html file

    var fileTemplate = '<!DOCTYPE html>' + '\n' +
    '<html lang="en">' + '\n' +
    ' <head> <meta charset="UTF-8">' + '\n' +
    '  <title> #{name} </title>' + '\n' +
    '  <script src="../../src/gojs/assets/js/jquery.min.js"></script>' + '\n' +
    '  <script src="../../src/gojs/assets/js/jquery-ui.min.js"></script>' + '\n' +
    '  <link rel="stylesheet" href="../../src/css/style.css" />' + '\n' +
    '  <script src="https://npmcdn.com/tether@1.2.4/dist/js/tether.min.js"></script>' + '\n' +
    '  <link rel="stylesheet" href="../../src/bootstrap-4.0.0-alpha.6-dist/css/bootstrap.min.css"/>' + '\n' +
    '  <script src="../../src/bootstrap-4.0.0-alpha.6-dist/js/bootstrap.min.js"></script>' + '\n' +
    ' </head> ' + '\n' +
    ' <body>' + '\n' +
    '  <div class="container-fluid">' + '\n' +
    '   #{content}' + '\n' +
    '   <footer> <p>&copy; SSMDWA - UNLP 2018</p> </footer>' + '\n' +
    '  </div>' + '\n' +
    ' </body>' + '\n' +
    '</html>';

    var myValues = { name: pages[p].name, content: content };
    var htmlCode = $.tmpl(fileTemplate, myValues);
    var fileName = pages[p].name;
    fileName = fileName.replace(' ', '_');
    fileName = fileName.toLowerCase();
    fileNameToSaveAs = fileName + ".html"

    //call function to create html file
    createFile(htmlCode, fileNameToSaveAs,'../public/view/', "html");

    //Create router for the new file
    if (pages[p].category == "MainPage"){ // For MainPage we set the route to '/'
     var routerTemplate = "router.get('/', function(req, res, next) {" + '\n' +
     "  res.sendFile(path.join(__dirname,'../public/view/', '/#{name}.html'));"+ '\n' +
     "});"+ '\n';
    } else {
     var routerTemplate = "router.get('/#{name}', function(req, res, next) {" + '\n' +
     "  res.sendFile(path.join(__dirname,'../public/view/', '/#{name}.html'));"+ '\n' +
     "});"+ '\n';
    }
    var routerValues = { name: fileName };
    var route = $.tmpl(routerTemplate, routerValues);
    routes = routes + route

  }

  //Create render.js file which contains routes to the new files
  var routerContentTemplate = "var express = require('express');" + '\n' +
  "var path = require('path');" + '\n' +
  "var router = express.Router();" + '\n' +
  "//below autogenerated routes" + '\n' +
  "#{routes}"+ '\n' +
  "module.exports = router;"+ '\n';

  var routerContentValues = { routes: routes };
  var routerContent = $.tmpl(routerContentTemplate, routerContentValues);
  //call function to create router for render
  createFile(routerContent, "render.js", '../routes/', "script" )

  //open tab index
  window.open(
    'http://localhost:3000/render',
    '_blank'
  );
}

function createFile(fileContent, fileName, dir, dataType ){
  var data = {};
  data.fileContent = fileContent;
  data.fileName = fileName
  data.dir = dir

  $.ajax({
    type: 'POST',
    dataType: dataType,
    data: JSON.stringify(data),
    contentType: "application/json; charset=utf-8",
    url: 'http://localhost:3000/createFile',
    converters: {
      'text json': true
    },
    success: function(data) {
      console.log('success')
    }
  });
}
